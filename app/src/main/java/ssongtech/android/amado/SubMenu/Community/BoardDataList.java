package ssongtech.android.amado.SubMenu.Community;

public class BoardDataList {
    public String id;
    public String subject;
    public String content;
    public String seo_title;
    public String mb_id;
    public String wr_name;
    public String wr_datetime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getSeo_title() {
        return seo_title;
    }

    public void setSeo_title(String seo_title) {
        this.seo_title = seo_title;
    }

    public String getMb_id() {
        return mb_id;
    }

    public void setMb_id(String mb_id) {
        this.mb_id = mb_id;
    }

    public String getWr_name() {
        return wr_name;
    }

    public void setWr_name(String wr_name) {
        this.wr_name = wr_name;
    }

    public String getWr_datetime() {
        return wr_datetime;
    }

    public void setWr_datetime(String wr_datetime) {
        this.wr_datetime = wr_datetime;
    }
}

